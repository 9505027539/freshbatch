package DateFormat.com;

public class DateFormat 
{
	public String StartDate;
	public String EndDate;
	public DateFormat() 
	{
		this.StartDate = StartDate;
		this.EndDate = EndDate;
	}
	public String getStartate() 
	{
		return StartDate;
	}
	public String getEndDate() 
	{
		return EndDate;
	}
	
	
}